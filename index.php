                  التوحد كما يُعرف باسم الذاتوية أو اضطراب التوحد الكلاسيكي. ويستخدم بعض الكتّاب كلمة "توحد أو ذاتوية" عند الإشارة إلى مجموعة من اضطرابات طيف التوحد أو مختلف اضطرابات النمو المتفشية، هو اضطراب النمو العصبي الذي يتصف بضعف التفاعل الاجتماعي، والتواصل اللفظي وغير اللفظي، وبأنماط سلوكية مقيدة ومتكررة. وتتطلب معايير التشخيص ضرورة أن تصبح الأعراض واضحة قبل أن يبلغ الطفل من العمر ثلاث سنوات. ويؤثر التوحد على عملية معالجة البيانات في المخ وذلك بتغييره لكيفية ارتباط وانتظام الخلايا العصبية ونقاط اشتباكها؛ ولم يفهم جيدًا كيف يحدث هذا الأمر. ويعتبر التوحد أحد ثلاثة اضطرابات تندرج تحت مرض طيف التوحد (ASDs)، ويكون الاضطرابان الثاني والثالث معًا متلازمة أسبيرجر، التي تفتقر إلى التأخر في النمو المعرفي وفي اللغة، وما يعرف باضطراب النمو المتفشي( يختصر عادة باسم PDD NOS) ويتم تشخيصه في حالة عدم تواجد معايير تحديد مرض التوحد أو متلازمة أسبرجر.

